def run():
    branch = "feature/POC-2-autenticacao-google-oauth"
    story = "POC-2"
    session = gate_logger.start_proposal(AGENT, "test-suite", f"pytest suite for {story}")
    log.info(AGENT, f"QA Agent iniciado para {story} na branch {branch}")

    # 1. Monta o arquivo de testes (scaffold + LLM section)
    log.info(AGENT, "Etapa 1/5: gerando scaffold deterministico")
    full_test = TEST_SCAFFOLD.replace(
        "# LLM_EDGE_CASES_PLACEHOLDER",
        "# [IA] Edge cases abaixo foram gerados por LLM\n" + LLM_EDGE_CASES
    )

    tests_dir = Path("tests")
    tests_dir.mkdir(exist_ok=True)
    (tests_dir / "__init__.py").write_text("", encoding="utf-8")
    (tests_dir / "conftest.py").write_text(CONFTEST, encoding="utf-8")
    test_file = tests_dir / "test_auth.py"
    test_file.write_text(full_test, encoding="utf-8")
    log.success(AGENT, f"Arquivos gerados: tests/test_auth.py, tests/conftest.py, tests/__init__.py")

    # 2. Validacao sintatica local (AST) — pytest real roda no CI
    log.info(AGENT, "Etapa 2/5: validacao sintatica (AST)")
    import ast
    try:
        tree = ast.parse(test_file.read_text(encoding="utf-8"))
        test_fns = [n for n in ast.walk(tree) if isinstance(n, ast.FunctionDef) and n.name.startswith("test_")]
        collected = len(test_fns)
        log.info(AGENT, f"AST validou: {collected} funcoes de teste detectadas", {
            "test_names": [fn.name for fn in test_fns]
        })
        if collected == 0:
            raise ValueError("Nenhum teste detectado")
    except (SyntaxError, ValueError) as e:
        log.error(AGENT, f"Validacao sintatica falhou: {e}", {"error": str(e)})
        gate_logger.record_decision(session, "rejected", feedback=f"AST invalido: {e}", jira_key=story)
        sys.exit(1)

    # 3. Atualiza requirements.txt (append se faltar)
    log.info(AGENT, "Etapa 3/5: atualizando requirements.txt")
    req_path = Path("requirements.txt")
    existing = req_path.read_text(encoding="utf-8") if req_path.exists() else ""
    needed = ["pytest", "pytest-asyncio", "httpx", "respx"]
    to_add = [p for p in needed if p not in existing]
    if to_add:
        new_req = existing.rstrip() + "\n" + "\n".join(to_add) + "\n"
        req_path.write_text(new_req, encoding="utf-8")
        log.info(AGENT, f"Adicionado ao requirements.txt: {to_add}")

    # 4. Commita na branch do PR #1
    log.info(AGENT, f"Etapa 4/5: commitando na branch {branch}")
    github_helper.commit_file(
        branch=branch,
        filepath="tests/__init__.py",
        content="",
        message=f"test({story}): add tests package marker"
    )
    github_helper.commit_file(
        branch=branch,
        filepath="tests/conftest.py",
        content=CONFTEST,
        message=f"test({story}): add pytest conftest"
    )
    github_helper.commit_file(
        branch=branch,
        filepath="tests/test_auth.py",
        content=full_test,
        message=f"test({story}): add auth OAuth test suite (happy + errors + edge cases)"
    )
    if to_add:
        github_helper.commit_file(
            branch=branch,
            filepath="requirements.txt",
            content=req_path.read_text(encoding="utf-8"),
            message=f"chore({story}): add pytest/respx to requirements"
        )

    # 5. Publica evidencia no Confluence
    log.info(AGENT, "Etapa 5/5: publicando evidencia no Confluence")
    deterministic_count = full_test.count("def test_") - LLM_EDGE_CASES.count("def test_")
    llm_count = LLM_EDGE_CASES.count("def test_")
    total = deterministic_count + llm_count

    confluence_body = f"""<h1>Evidencia de QA — {story}</h1>
<p><strong>Agente:</strong> QA Agent (POC Antigravity)</p>
<p><strong>Branch:</strong> <code>{branch}</code></p>
<p><strong>Data:</strong> gerado automaticamente</p>

<h2>Testes gerados</h2>
<ul>
  <li>Total: <strong>{total}</strong> casos</li>
  <li>Deterministicos (template): <strong>{deterministic_count}</strong></li>
  <li>Edge cases (IA): <strong>{llm_count}</strong></li>
</ul>

<h2>Estrategia</h2>
<p>Shift-left testing — testes commitados na mesma branch do PR de desenvolvimento (PR #1).</p>
<p>Geracao hibrida: scaffold deterministico (fixtures, happy + error paths) + enriquecimento por LLM para edge cases especificos de OAuth (Google timeout, unverified email, malformed id_token, CSRF state abuse).</p>

<h2>Cobertura dos casos</h2>
<h3>Happy path</h3>
<ul>
  <li>GET /health retorna 200</li>
  <li>GET /v1/auth/login redireciona para Google</li>
  <li>GET /v1/auth/callback com code valido autentica</li>
</ul>
<h3>Error paths</h3>
<ul>
  <li>Callback sem code retorna 400/422</li>
  <li>Callback com code invalido retorna 400/401</li>
  <li>State mismatch (CSRF) retorna 400/403</li>
  <li>Acesso sem token retorna 401</li>
</ul>
<h3>Edge cases (IA)</h3>
<ul>
  <li>Email nao verificado</li>
  <li>Timeout do Google</li>
  <li>redirect_uri nao-HTTPS</li>
  <li>id_token malformado</li>
  <li>State param com tamanho abusivo (DoS)</li>
</ul>

<h2>Links</h2>
<p>Pull Request: <a href="https://github.com/alvear/poc-antigravity/pull/1">PR #1 — Autenticacao Google OAuth</a></p>
<p>CI Pipeline: <a href="https://github.com/alvear/poc-antigravity/actions">GitHub Actions</a></p>
"""
    confluence_helper.create_page(
        title=f"QA Evidence — {story} — Google OAuth",
        content=confluence_body,
        parent_title="POC-Antigravity"
    )

    # 6. Fecha a sessao do gate_logger
    gate_logger.record_decision(session, "approved", jira_key=story)
    log.success(AGENT, f"QA completo: {total} testes, branch {branch}, evidencia publicada no Confluence")
    print(json.dumps({
        "agent": AGENT,
        "story": story,
        "branch": branch,
        "tests_total": total,
        "tests_deterministic": deterministic_count,
        "tests_llm": llm_count,
    }, indent=2))


if __name__ == "__main__":
    run()
