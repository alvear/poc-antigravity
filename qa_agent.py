def test_callback_without_code_returns_400():
    """Callback without authorization code should fail."""
    response = client.get("/v1/auth/callback")
    assert response.status_code in (400, 422)


@respx.mock
def test_callback_with_invalid_code_returns_401(mock_google_token_response):
    """Google rejecting the code should propagate as 401."""
    respx.post("https://oauth2.googleapis.com/token").mock(
        return_value=Response(400, json={"error": "invalid_grant"})
    )

    response = client.get(
        "/v1/auth/callback",
        params={"code": "expired_code", "state": "csrf_token_abc"},
    )
    assert response.status_code in (400, 401)


def test_callback_with_state_mismatch_returns_403():
    """CSRF: state param must match the one stored in session."""
    response = client.get(
        "/v1/auth/callback",
        params={"code": "valid_code", "state": "forged_state"},
    )
    assert response.status_code in (400, 403)


def test_protected_endpoint_without_token_returns_401():
    """Endpoints behind auth should reject unauthenticated requests."""
    response = client.get("/v1/auth/me")
    assert response.status_code in (401, 404)


