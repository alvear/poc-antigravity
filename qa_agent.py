def test_health_endpoint_returns_ok():
    """GET /health should return 200 with status ok."""
    response = client.get("/health")
    assert response.status_code == 200
    assert response.json().get("status") == "ok"


def test_login_endpoint_redirects_to_google():
    """GET /v1/auth/login should redirect to Google consent screen."""
    response = client.get("/v1/auth/login", follow_redirects=False)
    assert response.status_code in (302, 307)
    location = response.headers.get("location", "")
    assert "accounts.google.com" in location
    assert "client_id=" in location
    assert "response_type=code" in location


@respx.mock
def test_callback_with_valid_code_returns_session(
    mock_google_token_response, mock_google_userinfo
):
    """GET /v1/auth/callback with valid code should authenticate user."""
    respx.post("https://oauth2.googleapis.com/token").mock(
        return_value=Response(200, json=mock_google_token_response)
    )
    respx.get("https://openidconnect.googleapis.com/v1/userinfo").mock(
        return_value=Response(200, json=mock_google_userinfo)
    )

    response = client.get(
        "/v1/auth/callback",
        params={"code": "valid_auth_code", "state": "csrf_token_abc"},
        follow_redirects=False,
    )
    assert response.status_code in (200, 302)


